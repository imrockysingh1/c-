//Volume of a cuboid
#include<iostream>
using namespace std;
int main()
{
    int l,b,h,v;
    cout<<"enter length of cuboid :- ";
    cin>>l;
    cout<<"Enter breadth of cuboid :- ";
    cin>>b;
    cout<<"Enter height of cuboid :- ";
    cin>>h;
    v=l*b*h;
    cout<<"volume of cuboid is "<<v;
    return 0;
}